package model;

public class Data_Riwayat {
    Integer id;
    String name;
    String desc;
    Integer hari;
    Integer total;
    String t_pinjam;
    String t_tenggat;
    Integer user_id;
    String status;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public Integer getHari() {
        return hari;
    }

    public void setHari(Integer hari) {
        this.hari = hari;
    }

    public Integer getTotal() {
        return total;
    }

    public void setTotal(Integer total) {
        this.total = total;
    }

    public String getT_pinjam() {
        return t_pinjam;
    }

    public void setT_pinjam(String t_pinjam) {
        this.t_pinjam = t_pinjam;
    }

    public String getT_tenggat() {
        return t_tenggat;
    }

    public void setT_tenggat(String t_tenggat) {
        this.t_tenggat = t_tenggat;
    }

    public Integer getUser_id() {
        return user_id;
    }

    public void setUser_id(Integer user_id) {
        this.user_id = user_id;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

}
